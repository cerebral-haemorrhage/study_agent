"""硬编码工具 —— 城市天气查询"""

# 天气数据库
WEATHER_DB = {
    "北京": "晴天，25°C，湿度40%",
    "上海": "多云，28°C，湿度65%",
    "深圳": "阵雨，30°C，湿度80%",
    "杭州": "阴天，22°C，湿度55%",
    "成都": "小雨，20°C，湿度70%",
    "广州": "雷阵雨，32°C，湿度85%",
    "西安": "晴天，27°C，湿度35%",
    "武汉": "多云，29°C，湿度60%",
}


def get_weather(city: str) -> str:
    """查询城市天气"""
    return WEATHER_DB.get(city, f"未找到「{city}」的天气数据，请检查城市名称。")


# 给 LLM 看的工具描述
TOOLS_DESC = """
你可以使用以下工具：

工具名: get_weather
描述: 查询指定城市的天气
参数: city (字符串，城市名称，如"北京")

调用工具时，请按以下格式输出：
Action: get_weather
Action Input: {"city": "城市名"}
"""
