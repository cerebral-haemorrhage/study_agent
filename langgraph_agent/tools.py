"""
================================================================================
  tools.py — 工具定义
================================================================================

这里定义了 Agent 能用什么"工具"。
工具 = 一个函数，Agent 可以调它来获取外部信息。

【超级新手理解】
  你问 Agent "北京天气怎么样？"
    → Agent 脑子(LLM)不知道天气数据
    → 但它会说"我需要用 get_weather 这个工具查一下"
    → 系统帮它调 get_weather("北京")
    → 拿到结果 "晴天，25°C"
    → Agent 把结果告诉你

  @tool 装饰器就是给函数贴上"这是工具"的标签。
  贴上之后，LangChain 会自动读取函数名、参数名、参数类型、注释，
  生成一份"工具说明书"发给 LLM，让 LLM 知道怎么调这个工具。

【手搓版对比】
  手搓版: TOOLS_DESC 是一段手写的文字，告诉 LLM "有个工具叫 get_weather，参数是 city"
         LLM 返回字符串 "Action: get_weather\nAction Input: {\"city\": \"北京\"}"
         然后 parse_react_output() 用字符串匹配解析出来
  框架版: @tool 装饰器自动生成结构化描述
         LLM 返回结构化的 tool_calls = [{"name": "get_weather", "args": {"city": "北京"}}]
         不需要字符串解析！
"""
from langchain_core.tools import tool

# ———— 天气数据库（和手搓版一模一样） ————
WEATHER_DB = {
    "北京": "晴天，25°C，湿度40%",
    "上海": "多云，28°C，湿度65%",
    "深圳": "阵雨，30°C，湿度80%",
    "杭州": "阴天，22°C，湿度55%",
    "成都": "小雨，20°C，湿度70%",
    "广州": "雷阵雨，32°C，湿度85%",
    "西安": "晴天，27°C，湿度35%",
    "武汉": "多云，29°C，湿度60%",
}


# ———— 定义工具 ————
# @tool 是 LangChain 提供的装饰器（Decorator）
# 装饰器 = 给函数"穿上一件外套"，在不改变函数本身的情况下赋予它新能力
#
# @tool 做了什么：
#   1. 把 get_weather 包装成一个 LangChain Tool 对象
#   2. 自动提取函数签名（函数名、参数名、参数类型）
#   3. 自动提取 docstring（三引号里的说明文字）作为工具描述
#   4. 自动生成 JSON Schema 发给 LLM
#
# 所以下面这个函数写好后，LLM 自动知道：
#   - 有一个工具叫 "get_weather"
#   - 它需要传一个参数叫 "city"，类型是 str
#   - 它的功能是"查询指定城市的天气"
@tool
def get_weather(city: str) -> str:
    """
    查询指定城市的天气。

    Args:
        city: 城市名称，如"北京"、"上海"
    """
    # 从数据库里查，没查到就返回提示
    return WEATHER_DB.get(city, f"未找到「{city}」的天气数据，请检查城市名称。")


# ———— 工具列表 ————
# 把所有工具放进一个列表。
# 以后想加新工具（比如 get_time、search_web），
# 只需要写一个新函数 + @tool + 加进这个列表就行，
# 完全不需要改 graph.py！
TOOLS = [get_weather]
